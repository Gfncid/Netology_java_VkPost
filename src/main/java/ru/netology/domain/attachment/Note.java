package ru.netology.domain.attachment;

public class Note {
    private int id;
    private int owner_id;
    private String title;
    private String text;
    private int date;
    private int commentsInfo;
    private int read_commentsInfo;
    private String view_url;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getOwner_id() {
        return owner_id;
    }

    public void setOwner_id(int owner_id) {
        this.owner_id = owner_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getCommentsInfo() {
        return commentsInfo;
    }

    public void setCommentsInfo(int commentsInfo) {
        this.commentsInfo = commentsInfo;
    }

    public int getRead_commentsInfo() {
        return read_commentsInfo;
    }

    public void setRead_commentsInfo(int read_commentsInfo) {
        this.read_commentsInfo = read_commentsInfo;
    }

    public String getView_url() {
        return view_url;
    }

    public void setView_url(String view_url) {
        this.view_url = view_url;
    }
}
